# Task 2 — Function with Parameter
# ============================================================

def square(n):
    return n ** 2

print(square(5))   # 25
print(square(3))   # 9
print()


# ============================================================
# Task 3 — Required Arguments
# ============================================================

def info(name, age):
    print(f"Name: {name}, Age: {age}")

info("Ali", 20)

# Попытка вызвать без аргументов вызовет ошибку:
# info()  → TypeError: info() missing 2 required positional arguments: 'name' and 'age'
print()


# ============================================================
# Task 4 — Default Arguments
# ============================================================

def student(name, age=18):
    print(f"Name: {name}, Age: {age}")

student("Dias")       # age по умолчанию = 18
student("Dias", 22)   # age явно задан = 22

# Разница: если не передать age, используется значение по умолчанию (18).
# Если передать — используется переданное значение (22).
print()


# ============================================================
# Task 5 — Lambda + map
# ============================================================

nums = [1, 2, 3, 4]
squares = list(map(lambda x: x ** 2, nums))
print(squares)  # [1, 4, 9, 16]
